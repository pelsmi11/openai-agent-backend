CREATE EXTENSION vector;

CREATE TABLE personal_info (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    content TEXT NOT NULL, -- Original text or information about you
    embedding VECTOR(1536), -- OpenAI vector embedding
    category VARCHAR(100), -- New: data category (e.g., education, projects, interests)
    visibility BOOLEAN DEFAULT TRUE, -- New: useful to hide old data without deleting
    created_at TIMESTAMP DEFAULT NOW(),
    updated_at TIMESTAMP
);

CREATE INDEX personal_info_embedding_hnsw
  ON personal_info
  USING hnsw (embedding vector_l2_ops);


  select * from personal_info;
  select content, category from personal_info;


 create or replace function match_personal_info(
  query_embedding vector(1536),
  match_threshold float,
  match_count int
)
returns table (
  id uuid,
  content text,
  category text,
  created_at timestamp,
  similarity float
)
language sql
as $$
  select
    id,
    content,
    category,
    created_at,
    1 - (embedding <=> query_embedding) as similarity
  from personal_info
  where embedding <=> query_embedding < match_threshold
    and visibility = true
  order by embedding <=> query_embedding
  limit match_count;
$$;
 

